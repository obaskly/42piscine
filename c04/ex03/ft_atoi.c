int	ft_atoi(char *str)
{
	int	i;
	int	r;
	int	s;

	i = 0;
	r = 1;
	s = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			r = -r;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		s = (s * 10) + (str[i] - '0');
		i++;
	}
	return (s * r);
}
